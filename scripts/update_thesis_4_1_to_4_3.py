def main() -> None:
    updated = []
    skipped = []
    for path in DOC_PATHS:
        if not path.exists():
            continue
        try:
            doc = Document(str(path))
            apply_updates(doc)
            doc.save(str(path))
            updated.append(str(path))
        except PermissionError:
            skipped.append(str(path))

    print("UPDATED:")
    for item in updated:
        print(item)
    print("SKIPPED:")
    for item in skipped:
        print(item)


if __name__ == "__main__":
    main()
